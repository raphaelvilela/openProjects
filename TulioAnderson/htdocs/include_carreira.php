<div class="main_title">Carreira</div>

<p>Mineiros da cidade de Ibi&aacute;, Tulio &amp; Anderson cantam juntos
desde 1997.</p>

<p>Em 1999, gravaram seu primeiro CD, intitulado &ldquo;Eu Sei&rdquo;,
pela gravadora Zan Brasidisc.</p>

<p>Em 2005, a dupla sertaneja Tulio &amp; Anderson grava seu segundo CD
&ldquo;Em Busca da Felicidade&rdquo;, dessa vez com
produ&ccedil;&atilde;o independente. O repert&oacute;rio &eacute;
formado por m&uacute;sicas in&eacute;ditas, como, por exemplo, a
m&uacute;sica que deu o nome ao CD. Conta tamb&eacute;m com
regrava&ccedil;&otilde;es, dentre elas, a m&uacute;sica &ldquo;Um anjo
do C&eacute;u&rdquo;, que ganhou um novo estilo (forr&oacute;), foi
muito executada em r&aacute;dios de v&aacute;rias regi&otilde;es do
Brasil e, hoje, &eacute; sucesso na voz da dupla.</p>

<p>Em 2006, Tulio &amp; Anderson iniciam um intenso trabalho na cidade
de S&atilde;o Paulo, para divulga&ccedil;&atilde;o do CD e do trabalho
da dupla. Apresentaram-se em v&aacute;rios programas de TV regionais e
tamb&eacute;m na TV Aparecida, em rede nacional. Marcaram
presen&ccedil;a semanal em diversas casas de shows, entre elas o Villa
Country.</p>

<p>Em janeiro de 2007, Tulio &amp; Anderson desembarcam na Capital
Mineira, onde est&atilde;o at&eacute; hoje, apresentando-se em
v&aacute;rias casas de shows e restaurantes de Belo Horizonte, bem como
em festas e exposi&ccedil;&otilde;es nas v&aacute;rias cidades mineiras.</p>

<p>Em 2008, foram convidados pela prefeitura da cidade natal para
realiza&ccedil;&atilde;o de um show de comemora&ccedil;&atilde;o de
anivers&aacute;rio da cidade e tamb&eacute;m para a Festa do Pe&atilde;o
no Parque de Exposi&ccedil;&otilde;es. A m&uacute;sica in&eacute;dita da
dupla &ldquo;Num p&oacute; fal&aacute;&rdquo; esteve entre as 10
m&uacute;sicas mais tocadas nas r&aacute;dios da regi&atilde;o, ainda
sendo muito executada at&eacute; hoje.</p>
<p><br />
Em 2009, a dupla gravou seu CD ao vivo, na cidade de Lafaiete/MG, na
festa &ldquo;Arrai&aacute; do Povo&rdquo;, contando com um
p&uacute;blico superior a sete mil pessoas.</p>

<p>Nestes &uacute;ltimos anos, a dupla tem conquistado muitos f&atilde;s
na capital mineira e em cidades como Ouro Preto, Itabirito, Betim,
Contagem, Lagoa Santa, Matozinhos, Igarap&eacute;, onde tem realizado
shows em grandes eventos e casas noturnas. Destaque tamb&eacute;m para a
nova m&uacute;sica de trabalho &ldquo;100% Amor&rdquo;, que est&aacute;
entre as m&uacute;sicas mais pedidas em renomadas r&aacute;dios de Minas
Gerais, S&atilde;o Paulo e Goi&aacute;s.</p>

<p>O repert&oacute;rio da dupla &eacute; rico e variado, passando pelos
estilos sertanejo pop, rom&acirc;ntico e universit&aacute;rio,
forr&oacute;, vaner&atilde;o, country e modas de viola, agradando a
todos os tipos de p&uacute;blico.</p>

<p>Em todas as oportunidades de trabalho, Tulio &amp; Anderson mostram
profissionalismo, qualidade, alegria e carisma, encantando a todos com
suas can&ccedil;&otilde;es e deixando f&atilde;s por onde passam...</p>

